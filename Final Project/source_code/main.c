  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  { 
  switch (STATE) {
  case LED_OFF:
    GPIOA -> MODER &= ~GPIO_MODER_MODE5_Msk;    // Clear out Mode for Pin5
    GPIOA -> MODER |= GPIO_MODER_MODE5_0;       // Set Pin5 to GPIO Mode
    GPIOA -> ODR &= ~GPIO_ODR_OD5;    // LED OFF
  break;
  case LED_SOLID:
    GPIOA -> MODER &= ~GPIO_MODER_MODE5_Msk;    // Clear out Mode for Pin5
    GPIOA -> MODER |= GPIO_MODER_MODE5_0;       // Set Pin5 to GPIO Mode
    GPIOA -> ODR |= GPIO_ODR_OD5;     // LED ON
  break;
  case LED_BLINK_SLOW:
    GPIOA -> MODER &= ~GPIO_MODER_MODE5_Msk;    // Clear out Mode for Pin5
    GPIOA -> MODER |= GPIO_MODER_MODE5_0;       // Set Pin5 to GPIO Mode
    GPIOA -> ODR ^= GPIO_ODR_OD5;
    HAL_Delay(500);
  break;
  case LED_BLINK_FAST:
    GPIOA -> MODER &= ~GPIO_MODER_MODE5_Msk;    // Clear out Mode for Pin5
    GPIOA -> MODER |= GPIO_MODER_MODE5_0;       // Set Pin5 to GPIO Mode
    GPIOA -> ODR ^= GPIO_ODR_OD5;
    HAL_Delay(100);
  break;
  case LED_DIM_SLOW:
    GPIOA -> MODER &= ~GPIO_MODER_MODE5_Msk;    // Clear out Mode for Pin5
    GPIOA -> MODER |= GPIO_MODER_MODE5_1;       // Set Pin5 to AF Mode
    GPIOA -> AFR[0] |= 0x1;     // Set TIM2 AF in AFRL register for Pin5
    HAL_Delay(80); 
    if(pwm_value == 0) step = 100;
    if(pwm_value == 2000) step = -100;
    pwm_value += step;
    user_pwm_setvalue(pwm_value);       // Value to be loaded into the Capture Compare Register 
  break;
  case LED_DIM_FAST:
    HAL_Delay(25); 
    if(pwm_value == 0) step = 100;
    if(pwm_value == 2000) step = -100;
    pwm_value += step;
    user_pwm_setvalue(pwm_value);       // Value to be loaded into the Capture Compare Register 
  break;    
  }
  }
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

